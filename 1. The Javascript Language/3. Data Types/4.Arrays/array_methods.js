let arr = [11,22,335,64,75,86,970,68,79,20,];

// Push + Shift = Queue Data Structure

console.log("Push + Shift = Queue Data Structure");

console.log(arr.push('Apple'));
console.log(arr.push(1));
console.log(arr.push('Mango'));
console.log(arr.push(2));

console.log("Arr After Push: " , arr);

console.log(arr.shift());
console.log(arr.shift());
console.log(arr.shift());

console.log("Arr After Shift: " , arr);

// Push + Pop Stack Data Structure

console.log("Push + Shift = Stack Data Structure");

console.log(arr.push('Apple'));
console.log(arr.push(1));
console.log(arr.push('Mango'));
console.log(arr.push(2));

console.log("Arr After Push: " , arr);

console.log(arr.pop());
console.log(arr.pop());
console.log(arr.pop());

console.log("Arr After Pop: " , arr);